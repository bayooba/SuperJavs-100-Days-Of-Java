/**
 * Day 2
 * This program declares and initializes variables of different data types and prints
 * their values
 */

public class DataDeclaration {
    public static void main(String[] args) {
        String name = "Bayo";
        byte byteValue = 8;
        short shortValue = 16;
        char charValue = 'A';
        int intNum = 10;
        float floatNum = 2f;
        long longNum = 3L;
        double doubleNum = 3.56D;
        boolean status = true;

        System.out.println("Name --> " + name);
        System.out.println("Byte --> " + byteValue);
        System.out.println("Short --> " + shortValue);
        System.out.println("Char --> " + charValue);
        System.out.println("Integer --> " + intNum);
        System.out.println("Float --> " + floatNum);
        System.out.println("Long --> " + longNum);
        System.out.println("Double --> " + doubleNum);
        System.out.println("Boolean --> " + status);
    }
}
